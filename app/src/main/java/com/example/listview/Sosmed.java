package com.example.listview;

public class Sosmed {
    private int photo;
    private String nama;
    private String masa_pembuatan;
    private String deskripsi;

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getMasa_pembuatan() {
        return masa_pembuatan;
    }

    public void setMasa_pembuatan(String masa_pembuatan) {
        this.masa_pembuatan = masa_pembuatan;
    }
    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

}
